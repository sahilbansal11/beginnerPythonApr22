# Challenge 6: Different weather scenarios

weather = input()

if weather == 'rainy':
	print('Take an umbrella')
elif weather == 'sunny':
	print('Take a sunglass')
elif weather == 'windy':
	print('Take a jacket')
elif weather == 'foggy':
	print('Wear a mask')
else:
	# to cover any other cases
	print('Just go')

