# N = 5
# A = [1, 2, 3, 4, 5]
# -------------------
# x = A[N - 1]
# -------------------
# A[N - 1] = A[N - 2] => A[4] = A[3]
# A[N - 2] = A[N - 3] => A[3] = A[2]
# A[N - 3] = A[N - 4] => A[2] = A[1]
# A[N - 4] = A[N - 5] => A[1] = A[0]
# -------------------
# generalize the loop part:
# A[i] = A[i - 1], i = N - 1, N - 2, N - 3, N - 4
# start from i = N -1
# go till i = 1

# range(N - 1, 0, -1) => [N - 1, N - 2, ..., 1]
# -------------------
# A[0] = x

# -------------------

N = int(input())

A = list(map(int, input().split()))

x = A[N - 1]

i = N - 1
while i >= 1:
    A[i] = A[i - 1]
    i -= 1

A[0] = x

for y in A:
    print(y, end=' ')
