# empty tuple
a = tuple()
print(a)
print(type(a))

# empty list
b = list()
print(b)
print(type(b))
b.append(1)
print(b)

# empty tuple
c = ()
print(c)
print(type(c))

# incorrect syntax for tuples of length 1
d = ("sahil")
print(d)
print(type(d))

e = (1)
print(e)
print(type(e))

# correct syntax to create a tuple of length 1
f = (1,)
print(f)
print(type(f))
